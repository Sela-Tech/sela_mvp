    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();
        //noinspection SimplifiableIfStatement
        switch (id) {
            case R.id.action_settings:
                // TODO: Handle this
                break;
            case R.id.action_logout:
                SharedPreferences userPrefs = getSharedPreferences(AuthUtil.UserPrefs, MODE_PRIVATE);
                SharedPreferences.Editor userPrefsEditor = userPrefs.edit();
                userPrefsEditor.putBoolean(AuthUtil.LoggedIn, false);
                userPrefsEditor.apply();
                startActivity(new Intent(MainActivity.this, LoginRegisterActivity.class));
                finish();
                break;
        }
        return super.onOptionsItemSelected(item);
    }

    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();
        FragmentManager fragmentManager = getSupportFragmentManager();
        switch (id) {
            case R.id.nav_home:
                // TODO: Handle this
                break;
            case R.id.nav_projects:
                FragmentTransaction fragmentTransaction = fragmentManager.beginTransaction();
                ProjectListFragment fragment = new ProjectListFragment();
                fragmentTransaction.replace(R.id.content_main, fragment);
                fragmentTransaction.commit();
                break;
            case R.id.nav_search:
                // TODO: Handle this
                break;
            case R.id.nav_share:
                // TODO: Handle this
                break;
        }
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

